import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { Marked } from "marked";

const siteRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const repoRoot = resolve(siteRoot, "..");
const sourceDocsDir = resolve(repoRoot, "docs");
const generatedDir = resolve(siteRoot, "src/generated");
const generatedModulePath = resolve(generatedDir, "docs.ts");

const DOCS = [
  { source: "flashing.md", output: "flashing", title: "Flashing" },
  { source: "midi.md", output: "midi", title: "MIDI" },
  { source: "findings.md", output: "findings", title: "Firmware" },
  { source: "protocol.md", output: "protocol", title: "Protocol" },
  { source: "research.md", output: "research", title: "Research" },
  { source: "examples.md", output: "examples", title: "Examples" },
  { source: "acknowledgements.md", output: "acknowledgements", title: "Thanks" },
];

const markdownRenderer = new Marked({
  async: false,
  breaks: false,
  gfm: true,
  renderer: {
    html({ text }) {
      return escapeHtml(text);
    },
    link({ href, title, tokens }) {
      const label = this.parser.parseInline(tokens);
      const titleAttribute = title ? ` title="${escapeHtml(title)}"` : "";
      const externalAttributes = /^https?:\/\//i.test(href) ? ' target="_blank" rel="noopener noreferrer"' : "";
      return `<a href="${escapeHtml(href)}"${titleAttribute}${externalAttributes}>${label}</a>`;
    },
  },
});

await mkdir(generatedDir, { recursive: true });

const pages = [];
for (const doc of DOCS) {
  const markdown = await readFile(resolve(sourceDocsDir, doc.source), "utf8");
  pages.push({
    id: doc.output,
    title: doc.title,
    html: renderMarkdown(markdown),
  });
}

await writeFile(
  generatedModulePath,
  `// Generated by scripts/render-docs.mjs. Do not edit by hand.\n\nexport const GENERATED_DOC_PAGES = ${JSON.stringify(
    pages,
    null,
    2,
  )} as const;\n`,
);

console.log(`Rendered ${pages.length} docs.`);

function renderMarkdown(markdown) {
  return markdownRenderer.parse(markdown);
}

function escapeHtml(value) {
  return value.replace(/[&<>"']/g, (char) => {
    switch (char) {
      case "&":
        return "&amp;";
      case "<":
        return "&lt;";
      case ">":
        return "&gt;";
      case '"':
        return "&quot;";
      default:
        return "&#39;";
    }
  });
}
